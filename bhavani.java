    public static void main(String[] args) {
        int n = 10; // You can change this value to print odd numbers up to a different limit

        System.out.println("Odd numbers up to " + n + ":");

        for (int i = 1; i <= n; i += 2) {
            System.out.println(i);
        }
    }
